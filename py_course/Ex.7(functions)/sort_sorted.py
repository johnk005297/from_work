def task4_v1():
    import sys

    # считывание списка из входного потока (не меняйте переменную lst_in в программе)
    lst_in = list(map(str.strip, sys.stdin.readlines()))

    # здесь продолжайте программу (используйте список строк lst_in)
    order = [3,0,2,1]
    tpl = tuple(tuple(int(y) if y.isdecimal() else y for y in x.split(';')) for x in lst_in)

    t_sorted = tuple(tuple(x) for x in [sorted(x, key=lambda i: order[x.index(i)]) for x in tpl ])
    return t_sorted


