def task3_v1():

    lst = ['ножницы=100', 'котелок=500', 'спички=20', 'зажигалка=40', 'зеркальце=50']
    d = dict((c.split('=') for c in lst))

    print(*sorted(d, key=lambda x: int(d[x]), reverse=True))


